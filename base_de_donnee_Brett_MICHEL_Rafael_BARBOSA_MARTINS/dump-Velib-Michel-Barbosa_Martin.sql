CREATE VIEW AccesMairie AS
(
SELECT DISTINCT b.ville,b.code_station,b.dateR,b.nbb,v.nbv 
FROM
(SELECT ville,code_station,dateR,count(*) AS nbb FROM reservation_b NATURAL JOIN borne NATURAL JOIN station GROUP BY ville, code_station, dateR) AS b,
(SELECT ville,code_station,dateR,count(*) AS nbv FROM reservation_v NATURAL JOIN station GROUP BY ville, code_station, dateR) AS v
WHERE (b.ville=v.ville) AND (b.code_station = V.code_station)
);

